const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [language, setLanguage] = useState(() => {
    const stored = localStorage.getItem(STORAGE_KEY);
    return stored && STRINGS[stored] ? stored : "es";
  });

  const setLang = useCallback((lang) => {
    if (!STRINGS[lang]) return;
    setLanguage(lang);
    localStorage.setItem(STORAGE_KEY, lang);
  }, []);

  const t = useCallback(
    (key, vars) => {
      const parts = key.split(".");
      let current = STRINGS[language];
      for (const part of parts) {
        current = current?.[part];
      }
      const resolved = current ?? key;
      if (typeof resolved !== "string" || !vars) return resolved;
      return resolved.replace(/\{(\w+)\}/g, (match, token) => (
        token in vars ? String(vars[token]) : match
      ));
    },
    [language]
  );

  const value = useMemo(
    () => ({ language, setLanguage: setLang, t }),
    [language, setLang, t]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return ctx;
}

export function useI18nOptional() {
  return useContext(I18nContext);
}
